object CityDatabase {

    fun search(query: String): List<CityEntry> {
        val q = query.trim()
        if (q.isEmpty()) return emptyList()
        return ALL.filter {
            it.name.contains(q, ignoreCase = true) || it.province.contains(q, ignoreCase = true) || it.pinyin.contains(q, ignoreCase = true)
        }.take(10)
    }

    fun findNearest(lon: Double, lat: Double): CityEntry? {
        return ALL.minByOrNull {
            val d = (it.lon - lon); val e = (it.lat - lat); d * d + e * e
        }
    }

    data class CityEntry(
        val cityId: String, val name: String, val province: String, val pinyin: String = "",
        val lon: Double = 0.0, val lat: Double = 0.0
